public class Chip {

    private char chipColour;

    // Constructor
    public Chip(char chipColour) {
        setChipColour(chipColour);
    }

    // Getters and setters
    public char getChipColour() {
        return this.chipColour;
    }

    public void setChipColour(char chipColour) {
        this.chipColour = chipColour;
    }
}
